# ElCapitan2USB.sh
# Michael McMahon
# Copy Mac OS X El Capitan installer to an 8GB USB flash drive

# Once complete, you can plug it into another Mac.  After you press the power button, wait for the sound and press the ALT or OPTION key to bring up the boot menu.

sudo /Applications/Install\ OS\ X\ El\ Capitan.app/Contents/Resources/createinstallmedia --volume /Volumes/Untitled --applicationpath /Applications/Install\ OS\ X\ El\ Capitan.app --nointeraction

