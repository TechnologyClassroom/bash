# Mavericks2USB.sh
# Michael McMahon
# Copy Mac OS X Mavericks installer to an 8GB USB flash drive

# Once complete, you can plug it into another Mac.  After you press the power
# button, wait for the sound and press the ALT or OPTION key to bring up the
# boot menu.

sudo /Applications/Install\ OS\ X\ Mavericks.app/Contents/Resources/createinstallmedia --volume /Volumes/Untitled --applicationpath /Applications/Install\ OS\ X\ Mavericks.app --nointeraction

